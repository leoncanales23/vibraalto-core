def _normalize_row(row):
    trimmed = []
    for value in row:
        if value == -1:
            break
        trimmed.append(value)
    trimmed = trimmed[:15]  # Máx. 5 triángulos -> 15 índices
    return trimmed + [-1] * (16 - len(trimmed))


